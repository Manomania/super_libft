#include "../include/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned const char	*found;

	found = (unsigned const char *)s;
	while (n)
	{
		if (*found == (unsigned char)c)
			return ((void *)found);
		found++;
		n--;
	}
	return (NULL);
}
